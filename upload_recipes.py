# База эталонных ингредиентов с сайта (загружается из ingredients.json).
# Ключ — название в нижнем регистре, значение — оригинальный регистр.
_INGREDIENTS_DB: dict[str, str] = {}

def _load_ingredients_db():
    if not INGREDIENTS_FILE.exists():
        return
    try:
        with open(INGREDIENTS_FILE, encoding="utf-8") as f:
            names = json.load(f)
        for name in names:
            _INGREDIENTS_DB[name.lower().strip()] = name
        print(f"✓ База ингредиентов: {len(_INGREDIENTS_DB)} записей")
    except Exception as e:
        print(f"⚠ Не удалось загрузить {INGREDIENTS_FILE}: {e}")


def lookup_ingredient(raw_name: str) -> str:
    """
    Ищет наиболее подходящее эталонное название ингредиента из базы.
    Возвращает оригинальное название с сайта или пустую строку если не нашёл.
    Логика: точное совпадение → совпадение по главным словам → частичное.
    """
    if not _INGREDIENTS_DB:
        return ""
    key = raw_name.lower().strip()
    # 1. Точное совпадение
    if key in _INGREDIENTS_DB:
        return _INGREDIENTS_DB[key]
    # 2. Проверяем подстановки — если после замены есть точное совпадение
    substituted = _resolve_substitution(key)
    if substituted and substituted.lower() in _INGREDIENTS_DB:
        return _INGREDIENTS_DB[substituted.lower()]
    # 3. Ищем по словам: все слова из запроса должны быть в эталоне
    query_words = set(normalize_word(w) for w in key.split() if w)
    best_name = ""
    best_score = 0.0
    for db_key, db_name in _INGREDIENTS_DB.items():
        db_words = set(normalize_word(w) for w in db_key.split() if w)
        if not db_words:
            continue
        common = query_words & db_words
        if not common:
            continue
        # Доля совпавших слов из запроса / из эталона (F1-подобная метрика)
        precision = len(common) / len(query_words)
        recall    = len(common) / len(db_words)
        score = 2 * precision * recall / (precision + recall) if (precision + recall) else 0
        if score > best_score:
            best_score = score
            best_name = db_name
    # Возвращаем только если совпадение достаточно уверенное
    return best_name if best_score >= 0.6 else ""


def _find_closest_in_db(raw_name: str) -> str:
    """
    Fallback-поиск в базе ингредиентов когда автокомплит ничего не вернул.
    Дополнительно умеет обрабатывать диапазоны процентов:
    "сливки 33-35%" → ищет все "сливки N%" в базе и берёт с ближайшим N.
    Возвращает оригинальное название из базы или пустую строку.
    """
    if not _INGREDIENTS_DB:
        return ""

    # Сначала стандартный lookup (он уже ищет по словам с F1-скором)
    result = lookup_ingredient(raw_name)
    if result:
        return result

    # Специальная обработка диапазонов процентов: "33-35%" → mid=34
    pct_range = re.search(r'(\d+)\s*[-–]\s*(\d+)\s*%', raw_name)
    if not pct_range:
        return ""

    lo, hi = int(pct_range.group(1)), int(pct_range.group(2))
    mid = (lo + hi) / 2
    # Базовое имя без процента: "сливки 33-35%" → "сливки"
    base = re.sub(r'\s*\d+\s*[-–]\s*\d+\s*%', '', raw_name).strip().lower()
    base_words = set(normalize_word(w) for w in base.split() if w)
    if not base_words:
        return ""

    best_name = ""
    best_diff = float('inf')
    for db_key, db_name in _INGREDIENTS_DB.items():
        db_words = set(normalize_word(w) for w in db_key.split() if w)
        # Базовые слова должны совпадать
        if not base_words.issubset(db_words) and not base_words & db_words:
            continue
        # Ищем процент в записи базы
        db_pct = re.search(r'(\d+(?:\.\d+)?)\s*%', db_key)
        if not db_pct:
            continue
        diff = abs(float(db_pct.group(1)) - mid)
        if diff < best_diff:
            best_diff = diff
            best_name = db_name

    # Если нашли хоть что-то — возвращаем, даже если % сильно отличается
    if best_name:
        return best_name

    # Последний резерв: ищем по базовому имени без %
    return lookup_ingredient(base)


