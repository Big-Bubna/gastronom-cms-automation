def normalize_word(word: str) -> str:
    """
    Приводит слово к нормальной форме (именительный падеж, единственное число).
    Например: "помидоры" → "помидор", "сосисок" → "сосиска".
    Если pymorphy3 не установлен — возвращает слово как есть.
    """
    if not HAS_MORPH or not word:
        return word.lower()
    try:
        return _morph.parse(word)[0].normal_form
    except Exception:
        return word.lower()


def normalize_phrase(phrase: str) -> str:
    """Нормализует все слова во фразе."""
    return " ".join(normalize_word(w) for w in phrase.split())


