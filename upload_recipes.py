            print("=" * 70)
            print(f"Ошибка: {e}\n")
            print("Chrome должен быть запущен в режиме отладки.")
            if sys.platform == "win32":
                print("\nНа Windows: открой cmd (или PowerShell) и выполни:")
                print('   "C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe" ^')
                print('     --remote-debugging-port=9222 ^')
                print('     --user-data-dir="%USERPROFILE%\\chrome-debug-profile"')
                print("\nЕсли Chrome установлен в другом месте, см. README-Windows.md")
            elif sys.platform == "darwin":
                print("\nНа macOS:")
                print("1. Полностью закрой Chrome (⌘+Q, не просто закрытие окна)")
                print("2. Открой Терминал и выполни:")
